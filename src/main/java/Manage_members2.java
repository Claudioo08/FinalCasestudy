    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    int row = Table1.getSelectedRow();
    
    if (row != -1) {
        // Update the row with data from JTextFields
        Table1.setValueAt(txtMID.getText(), row, 0);     // Member ID
        Table1.setValueAt(txtFName.getText(), row, 1);   // First Name
        Table1.setValueAt(txtLName.getText(), row, 2);   // Last Name
        Table1.setValueAt(txtEmail.getText(), row, 3);   // Email
        Table1.setValueAt(txtPassword.getText(), row, 4); // Password

        JOptionPane.showMessageDialog(this, "Member updated.");
    } else {
        JOptionPane.showMessageDialog(this, "Select a row to update.");
    }
    if (validateFields()) {
        // Your update logic here
        JOptionPane.showMessageDialog(this, "Member updated successfully!");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

