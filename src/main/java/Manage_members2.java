    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     String searchTerm = txtSearch.getText().toLowerCase();
    DefaultTableModel model = (DefaultTableModel) Table1.getModel();
    boolean found = false;

    for (int i = 0; i < model.getRowCount(); i++) {
        String memberId = model.getValueAt(i, 0).toString().toLowerCase();
        String firstName = model.getValueAt(i, 1).toString().toLowerCase();
        
        
        if (memberId.contains(searchTerm) || firstName.contains(searchTerm)) {
            Table1.setRowSelectionInterval(i, i);
            found = true;
            break;
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(this, "Member not found.");
    }
    }//GEN-LAST:event_btnSearchActionPerformed

