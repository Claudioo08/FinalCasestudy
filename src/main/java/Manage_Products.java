    private void btnDelete2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelete2ActionPerformed
            DefaultTableModel model = (DefaultTableModel) table2.getModel();
    int selectedRow = table2.getSelectedRow();
    
    if (selectedRow != -1) {
        model.removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Member deleted.");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row to delete.");
    }
    }//GEN-LAST:event_btnDelete2ActionPerformed

    private void btnUpdate2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate2ActionPerformed
    int row = table2.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
        return;
    }

    // Update selected row
    table2.setValueAt(txtShoeID.getText(), row, 0);
    table2.setValueAt(txtShoeSize.getText(), row, 1);
    table2.setValueAt(txtShoeName.getText(), row, 2);
    table2.setValueAt(txtStock.getText(), row, 3);
    table2.setValueAt(txtPrice.getText(), row, 4);

    JOptionPane.showMessageDialog(this, "Product updated successfully.");
    }//GEN-LAST:event_btnUpdate2ActionPerformed

